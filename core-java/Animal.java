class Animal{
	private String name;
	private String colour;
	
	public Animal(){
		this.name = "Lion";
		this.colour = "Orange";
	}
	
	public void setName(String name){
		this.name = name;
	}
	
	public String getName(){
		return name;
	}
	public void setColour(String colour){
		this.colour = colour;
	}
	public String getColour(){
		return colour;
	}
}
		
	