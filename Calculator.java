class Calculator{
	static int num1=20;
    static int num2=10;
    static int num3=5;
    static int num4=2;
    static int num5=3;




public static int addition(){
	int total=num1+num2+num3+num4+num5;
	return total;
}
public static int substraction(){
	int total=num1-num2-num3-num4-num5;
	return total;
}
public static int mul(){
	int total=num1*num2*num3*num4*num5;
	return total;
}
public static int div(){
	int total=num1/num2/num3/num4/num5;
	return total;
}
public static int avg(){
	int total=(num1+num2+num3+num4+num5)/5;
	return total;
}
}