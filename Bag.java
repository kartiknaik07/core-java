class Bag{
	
		private String colour = "Black";
		private String type = "School Bag";
		
		
		public void setColour(String colour){
			this.colour = colour;
		}
		public String getColour(){
			return colour;
		}
		public void setType(String type){
			this.type = type;
		}
		public String getType(){
			return type;
		}
}