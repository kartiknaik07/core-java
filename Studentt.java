class Studentt {
	String name;
	String department;

	public Studentt(String name, String department) {
		this.name = name;
		this.department = department;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setDepartment(String department) {
		this.department = department;
	}

	public String getDepartment() {
		return department;
	}
}


