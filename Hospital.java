class Hospital{
	private String name;
	private String location;
	private int numBeds;
	
	
	public void setName(String a){
		name = a;
	}
	public String getName(){
		return name;
	}
	public void setLocation(String b){
		location = b;
	}
	public String getLocation(){
		return location;
	}
	public void setBeds(int c){
		numBeds = c;
	}
	public int getBeds(){
		return numBeds;
	}
}
		
	