class Flower{
	String name;
	String colour;
	
	public Flower(String name,String colour){
		this.name = name;
		this.colour = colour;
	}
	public void setName(String name){
		this.name = name;	
	}
	public String getName(){
		return name;
	}
	public void setColour(String colour){
		this.colour = colour;
	}		
	public String getColour(){
		return colour;
	}
	
}